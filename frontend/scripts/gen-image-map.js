#!/usr/bin/env node
// Scans assets/ingredients/*.png and assets/dishes/*.png and regenerates
// src/generated-image-maps.ts with static require() entries.
// Run after adding/removing images:  node scripts/gen-image-map.js
const fs = require("fs");
const path = require("path");

function scan(dir, rel) {
  const abs = path.join(__dirname, "..", "assets", dir);
  if (!fs.existsSync(abs)) return [];
  return fs
    .readdirSync(abs)
    .filter((f) => /\.(png|webp|jpg)$/i.test(f))
    .map((f) => ({ id: f.replace(/\.(png|webp|jpg)$/i, ""), req: `require("../assets/${dir}/${f}")` }));
}

const ing = scan("ingredients");
const dish = scan("dishes");

const out = `// AUTO-GENERATED by scripts/gen-image-map.js — do not edit by hand.
/* eslint-disable */
export const INGREDIENT_IMAGES: Record<string, any> = {
${ing.map((e) => `  ${JSON.stringify(e.id)}: ${e.req},`).join("\n")}
};

export const DISH_IMAGES: Record<string, any> = {
${dish.map((e) => `  ${JSON.stringify(e.id)}: ${e.req},`).join("\n")}
};
`;
fs.writeFileSync(path.join(__dirname, "..", "src", "generated-image-maps.ts"), out);
console.log(`Wrote image maps: ${ing.length} ingredients, ${dish.length} dishes`);
